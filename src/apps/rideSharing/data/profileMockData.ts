// Helper to safely generate emoji flag from 2-letter ISO code (e.g. "US" -> 🇺🇸)
// This is immune to file encoding corruption and broken unicode sequences.
function getFlagEmoji(countryCode: string) {
  return countryCode
    .toUpperCase()
    .replace(/./g, (char) => String.fromCodePoint(char.charCodeAt(0) + 127397));
}

